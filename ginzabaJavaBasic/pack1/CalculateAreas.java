package pack1;

public class CalculateAreas {
	// method signature   
	public double rectangleArea(double length,double width) {
	
	double  rectArea = length  * width;
	
	return rectArea;
	
	}
	
	public double  circleArea(double radius) {
		
		double circArea = 3.14  * radius * radius;
		
		return circArea;
	}

}
